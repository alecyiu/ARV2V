"""Themed prompt presets for the grid demo (4 themes; matches 4 non-GT columns).

Prompts are written in Chinese — Wan 2.1 was trained primarily on
Chinese text-video pairs and steers noticeably harder when prompted in
Chinese than in English. Each preset's negative explicitly fights the
*other three* themes plus generic Wan failure modes (color cast, blur,
deformed cars, watermarks, JPEG residue, frozen frames).

`name` and `label` stay in English so they remain readable in column
headers and filenames.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class PromptPreset:
    name: str
    label: str
    prompt: str
    negative_prompt: str


